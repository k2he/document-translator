    path = Path("work/translated_segments.json")
    with open(path) as f:
        segs = json.load(f)

    patched = 0
    for seg in segs:
        if seg["id"] in PATCHES:
            seg["translation"] = PATCHES[seg["id"]]
            patched += 1

    with open(path, "w", encoding="utf-8") as f:
        json.dump(segs, f, ensure_ascii=False, indent=2)

